#include<bits/stdc++.h>
using namespace std;
int main(){
    int t;
    cin>>t;
    while(t--){
        bool found=false;
        string s="codeforces";
        char c;
        cin>>c;
        for(int i=0; i<10; i++){
            if(s[i]==c){
                found=true;
                break;
            }
            else{
                found=false;
            }
        }
        if(found){
            cout<<"YES"<<"\n";
        }
        else{
            cout<<"NO"<<"\n";
        }
    }
}
